                    <p>Do primitivo videogame de Higinbotham, o game evoluiu da simulação de jogos convencionais, tais como os de damas e xadrez, para disputas e confrontos expositivos.</p>
                    <p>Em 1966, o engenheiro eletrônico Ralph Baer, alemão refugiado nos Estados Unidos durante a Segunda Guerra Mundial, considerado hoje o pai dos consoles de games, vislumbrou a oportunidade de criar um equipamento que processasse jogos eletrônicos por meio de sua veiculação na televisão, com poucos recursos financeiros.</p>
                    <p>Isto ocorreu quando Baer trabalhava para a Sander Associates. Contando com a contribuição de seus amigos e companheiros de trabalho, o primeiro fruto de suas experiências foi o ‘chasing game’, elaborado em 1967, um rústico game de Ping Pong, no qual duas figuras no formato quadrado podiam ser manuseadas pelo competidor, e assim deslocadas ao longo da tela.</p>
                    <p>Seu inventor o patenteou logo em seguida e, um ano depois, expôs o primeiro modelo do videogame, designado de Brown Box, o qual consistia de disputas de futebol, vôlei e tiro. Portanto, não foi o modelo Atari o primeiro game a ser comercializado no mercado de videogames, mas sim este ancestral dos games.</p>
